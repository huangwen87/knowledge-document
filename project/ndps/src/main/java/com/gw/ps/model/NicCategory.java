package com.gw.ps.model;

import org.apache.ibatis.type.Alias;

/**
 * 行业分类预判
 * 
 * @author JohnmyWork
 * 
 */
@Alias("NicCategory")
public class NicCategory {
	private long newsCode; // 新闻ID
	private String result; // 分类结果
	private String categoryName;// 类别名称
	private String logTime; // 记录时间
	private String title;// 标题
	private String content;// 新闻内容

	public long getNewsCode() {
		return newsCode;
	}

	public void setNewsCode(long newsCode) {
		this.newsCode = newsCode;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}

	public String getLogTime() {
		return logTime;
	}

	public void setLogTime(String logTime) {
		this.logTime = logTime;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	@Override
	public String toString() {
		return "NicCategory [newsCode=" + newsCode + ", result=" + result + ", categoryName=" + categoryName + ", logTime=" + logTime + ", title=" + title + ", content=" + content.length()
				+ "]";
	}

}
