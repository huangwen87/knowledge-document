package com.gw.ps.monitor.yt;

/**
 * @function：统计去重模块  流入、流入量
 * @author Darwen
 * @data:2013-10-14下午4:25:36
 *
 */
public class TotalMessage {
	
	private int cmd;
	private int inflow;
	private int outflow;
	
	public int getCmd() {
		return cmd;
	}
	public void setCmd(int cmd) {
		this.cmd = cmd;
	}	
	public int getInflow() {
		return inflow;
	}
	public void setInflow(int inflow) {
		this.inflow = inflow;
	}
	public int getOutflow() {
		return outflow;
	}
	public void setOutflow(int outflow) {
		this.outflow = outflow;
	}
	
	@Override
	public String toString() {
		return "TotalMessage [cmd=" + cmd + ", inflow=" + inflow + ", outflow="
				+ outflow + "]";
	}

}
