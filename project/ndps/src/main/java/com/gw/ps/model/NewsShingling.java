package com.gw.ps.model;

import org.apache.ibatis.type.Alias;

@Alias("NewsShingling")
public class NewsShingling {
	private long id;
	private int wordnum;
	private String shingling;
	private String time;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public int getWordnum() {
		return wordnum;
	}

	public void setWordnum(int wordnum) {
		this.wordnum = wordnum;
	}

	public String getShingling() {
		return shingling;
	}

	public void setShingling(String shingling) {
		this.shingling = shingling;
	}

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	@Override
	public String toString() {
		return "NewsShingling [id=" + id + ", wordnum=" + wordnum + ", shingling=" + shingling + ", time=" + time + "]";
	}

}
