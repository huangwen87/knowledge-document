package com.gw.ncps.model;

import org.apache.ibatis.type.Alias;

/** 
 * @function：情感数据源   流入、流出量
 * @author Darwen
 * @data:2013-10-16下午2:36:27
 *
 */
@Alias("NewsSentimentVolume")
public class NewsSentimentVolume {
	
	private long inflow;  //流入量
	private long outflow;  //流出量
	
	public long getInflow() {
		return inflow;
	}
	public void setInflow(long inflow) {
		this.inflow = inflow;
	}
	public long getOutflow() {
		return outflow;
	}
	public void setOutflow(long outflow) {
		this.outflow = outflow;
	}
	
	@Override
	public String toString() {
		return "NewsSentimentVolume [inflow=" + inflow + ", outflow=" + outflow
				+ "]";
	}

}
