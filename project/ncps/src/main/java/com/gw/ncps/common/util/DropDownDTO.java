package com.gw.ncps.common.util;

/**
 * 下拉列表对象
 * 
 * @author zqgame
 * 
 */
public class DropDownDTO {

	private String value;

	private String lable;

	private String code;

	public DropDownDTO() {
	}

	public DropDownDTO(String value, String lable) {
		this.value = value;
		this.lable = lable;
	}
	
	public DropDownDTO(String value, String lable,String code) {
		this.value = value;
		this.lable = lable;
		this.code = code;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getLable() {
		return lable;
	}

	public void setLable(String lable) {
		this.lable = lable;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

}
