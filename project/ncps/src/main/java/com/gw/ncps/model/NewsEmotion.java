package com.gw.ncps.model;

import org.apache.ibatis.type.Alias;

/**
 * 新闻情感查询结果
 * 
 * @author Darwen
 * */
@Alias("NewsEmotion")
public class NewsEmotion {

	private int id;
	private String title;
	private String posORneg;
	private String timestamp;
	private String classify;
	private String organization;
	private String result;
	private String publishdate;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getPosORneg() {
		return posORneg;
	}

	public void setPosORneg(String posORneg) {
		this.posORneg = posORneg;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public String getClassify() {
		return classify;
	}

	public void setClassify(String classify) {
		this.classify = classify;
	}

	public String getOrganization() {
		return organization;
	}

	public void setOrganization(String organization) {
		this.organization = organization;
	}

	public String getResult() {
		return result;
	}

	public void setResult(String result) {
		this.result = result;
	}

	@Override
	public String toString() {
		return "NewsEmotion [id=" + id + ", title=" + title + ", posORneg=" + posORneg + ", timestamp=" + timestamp + ", classify=" + classify + ", organization=" + organization
				+ "]";
	}

	public String getPublishdate() {
		return publishdate;
	}

	public void setPublishdate(String publishdate) {
		this.publishdate = publishdate;
	}
	
	

}
