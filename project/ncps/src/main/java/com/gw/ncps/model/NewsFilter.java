package com.gw.ncps.model;

import org.apache.ibatis.type.Alias;

/**
 * 新闻过滤结果
 * 
 * @author JohnmyWork
 * 
 */
@Alias("NewsFilter")
public class NewsFilter {
	private int filterId;
	private int originId;
	private String content;
	private String timestamp;
	private int isdup;
	private int istrash;
	private int iscd;
	private String title;

	private String timeIn;
	private String timeOut;
	private String timedesc;

	private String groupName; // 网站
	private String siteName; // 频道
	private String channel; // 栏目
	private String webgrab; //是否定点抓取

	public int getFilterId() {
		return filterId;
	}

	public void setFilterId(int filterId) {
		this.filterId = filterId;
	}

	public int getOriginId() {
		return originId;
	}

	public void setOriginId(int originId) {
		this.originId = originId;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getTimestamp() {
		return timestamp;
	}

	public void setTimestamp(String timestamp) {
		this.timestamp = timestamp;
	}

	public int getIsdup() {
		return isdup;
	}

	public void setIsdup(int isdup) {
		this.isdup = isdup;
	}

	public int getIstrash() {
		return istrash;
	}

	public void setIstrash(int istrash) {
		this.istrash = istrash;
	}

	public int getIscd() {
		return iscd;
	}

	public void setIscd(int iscd) {
		this.iscd = iscd;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getTimeIn() {
		return timeIn;
	}

	public void setTimeIn(String timeIn) {
		this.timeIn = timeIn;
	}

	public String getTimeOut() {
		return timeOut;
	}

	public void setTimeOut(String timeOut) {
		this.timeOut = timeOut;
	}

	public String getTimedesc() {
		return timedesc;
	}

	public void setTimedesc(String timedesc) {
		this.timedesc = timedesc;
	}

	public String getGroupName() {
		return groupName;
	}

	public void setGroupName(String groupName) {
		this.groupName = groupName;
	}

	public String getSiteName() {
		return siteName;
	}

	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}

	public String getChannel() {
		return channel;
	}

	public void setChannel(String channel) {
		this.channel = channel;
	}

	public String getWebgrab() {
		return webgrab;
	}

	public void setWebgrab(String webgrab) {
		this.webgrab = webgrab;
	}
	
	

}
