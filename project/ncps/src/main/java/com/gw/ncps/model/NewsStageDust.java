package com.gw.ncps.model;

import org.apache.ibatis.type.Alias;

/**
 * @function：阶段性 纯垃圾、含垃圾数据统计
 * @author Darwen
 * @data:2013-9-9下午3:34:07
 *
 */
@Alias("NewsStageDust")
public class NewsStageDust {
	 
	private int sum;
	private int containDust;   //含垃圾
	private int pureDust;      //纯垃圾
	
	
	public int getSum() {
		return sum;
	}
	public void setSum(int sum) {
		this.sum = sum;
	}
	public int getContainDust() {
		return containDust;
	}
	public void setContainDust(int containDust) {
		this.containDust = containDust;
	}
	public int getPureDust() {
		return pureDust;
	}
	public void setPureDust(int pureDust) {
		this.pureDust = pureDust;
	}
	
	

}
